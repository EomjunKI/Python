# 02.01 Operator

# arithmetic operator
a = 5
b = 2

print(a + b)     # additon
print(a - b)     # subtraction
print(a * b)     # multiplication
print(a / b)     # division
print(a ** b)    # exponentiation
print(a % b)     # remainder
print(a // b)    # quotient

# comparison operator
print(10 > 3)     # over / under
print(3 >= 10)    # more than / less than
print(3 == 3)     # equal
print(3 != 3)     # not equal